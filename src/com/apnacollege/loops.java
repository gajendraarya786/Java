package com.apnacollege;
public class loops  {
    public static void main(String[] args){
        System.out.println("hello");
        //Loops
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        for(int j = 100; j>=1; j--){
            System.out.println(j);
        }

    }

}